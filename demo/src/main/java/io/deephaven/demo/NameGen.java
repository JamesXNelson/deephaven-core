    private static final String MY_NAME;
    private static final Set<String> usedNames;
    private static final Random rnd;

    static {
        usedNames= new ConcurrentHashSet<>();
        rnd = new Random();
        // every instance running the controller server should start somewhere different in the
        // adjective - noun pairing (and remain atomic from our starting point).
        adjectiveCnt = new AtomicInteger(rnd.nextInt(ADJECTIVES.length));
        nounCnt = new AtomicInteger(rnd.nextInt(NOUNS.length));
        // TODO: we should really look in env var for this before we make a new one...
        MY_NAME = newName();
        // This may seem silly, but this log message will allow us to reverse-engineer the starting
        // indices,
        // in case there is ever a bug where we need to recreate specific machine names.
        System.out.println("Initialized NameSources from " + MY_NAME);
    }

    public static String getMyName() {
        return MY_NAME;
    }

    public static String newName() {
//        return newName(adjectiveCnt.incrementAndGet(), nounCnt.incrementAndGet());
        return newName(rnd.nextInt(), rnd.nextInt());
    }

    public static String newName(int adjInd, int nounInd) {
        int myAdj = Math.abs(adjInd) % ADJECTIVES.length;
        int myNoun = Math.abs(nounInd) % NOUNS.length;
        String name;
        do {
            name = ADJECTIVES[myAdj] + "-" + NOUNS[myNoun];
        } while (!usedNames.add(name));
        return name;
    }


